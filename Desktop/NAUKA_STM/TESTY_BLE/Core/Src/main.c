/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance != USART1) return;

  if (rx1_index < RX_BUFFER_SIZE - 1)
  {
    rx1_buffer[rx1_index++] = rx1_char;

    if (rx1_char == '\n')  // zakończenie wiadomości
    {
      rx1_buffer[rx1_index] = '\0';  // zakończ string

      if (strcmp(rx1_buffer, "RESET\r\n") == 0) {
    	  // Wyczyść wszystkie dane i wróć do "startu"
    	     __disable_irq();  // zabezpiecz przed przerwaniami

    	     isStarted = false;
    	     distance = 0;
    	     current_mA = 0;

    	     HAL_GPIO_WritePin(LEDZIK_GPIO_Port, LEDZIK_Pin, GPIO_PIN_RESET);

    	     __enable_irq();

    	     HAL_UART_Transmit(&huart1, (uint8_t *)"Gotowe. Wciśnij przycisk START.\r\n", strlen("Gotowe. Wciśnij przycisk START.\r\n"), HAL_MAX_DELAY);

      }
      else
      {
        HAL_UART_Transmit(&huart1, (uint8_t *)"UNKNOWN\n", 8, HAL_MAX_DELAY);
      }

      // Reset bufora
      rx1_index = 0;
      memset(rx1_buffer, 0, sizeof(rx1_buffer));
    }
  }

  // Odbiór kolejnego znaku
  HAL_UART_Receive_IT(&huart1, &rx1_char, 1);
}


